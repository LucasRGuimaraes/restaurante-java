package exemplo1;

public class Pessoa {
	
	String nome;
	int idade;
	float peso;
	String corDaPele;
	String corDoCabelo;
	String estadoCivil;
	boolean estaEmpregado;
	String sexo;
	float saldoBancario;
	int numeroDeDedos;
	

	public String getNome() {
		return nome;
	}


	public void setNome(String nome) {
		this.nome = nome;
	}


	public int getIdade() {
		return idade;
	}


	public void setIdade(int idade) {
		this.idade = idade;
	}


	public double getPeso() {
		return peso;
	}


	public void setPeso(float peso) {
		this.peso = peso;
	}


	public String getCorDaPele() {
		return corDaPele;
	}


	public void setCorDaPele(String corDaPele) {
		this.corDaPele = corDaPele;
	}


	public String getCorDoCabelo() {
		return corDoCabelo;
	}


	public void setCorDoCabelo(String corDoCabelo) {
		this.corDoCabelo = corDoCabelo;
	}


	public String getEstadoCivil() {
		return estadoCivil;
	}


	public void setEstadoCivil(String estadoCivil) {
		this.estadoCivil = estadoCivil;
	}


	public boolean isEstaEmpregado() {
		return estaEmpregado;
	}


	public void setEstaEmpregado(boolean estaEmpregado) {
		this.estaEmpregado = estaEmpregado;
	}


	public String getSexo() {
		return sexo;
	}


	public void setSexo(String sexo) {
		this.sexo = sexo;
	}


	public float getSaldoBancario() {
		return saldoBancario;
	}


	public void setSaldoBancario(float saldoBancario) {
		this.saldoBancario = saldoBancario;
	}


	public int getNumeroDeDedos() {
		return numeroDeDedos;
	}


	public void setNumeroDeDedos(int numeroDeDedos) {
		this.numeroDeDedos = numeroDeDedos;
	}
}
